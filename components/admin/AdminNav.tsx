'use client';

import Link from 'next/link';
import { usePathname } from 'next/navigation';
import { cn } from '@/lib/utils';

const navItems = [
  { href: '/admin', label: 'Dashboard', icon: HomeIcon },
  { href: '/admin/platforms', label: 'Platforms', icon: LayersIcon },
  { href: '/admin/features', label: 'Features', icon: ListIcon },
  { href: '/admin/comparisons', label: 'Comparisons', icon: GridIcon },
];

export function AdminNav() {
  const pathname = usePathname();
  
  return (
    <nav className="flex-1 p-4">
      <ul className="space-y-1">
        {navItems.map((item) => {
          const isActive = pathname === item.href || 
            (item.href !== '/admin' && pathname.startsWith(item.href));
          
          return (
            <li key={item.href}>
              <Link
                href={item.href}
                className={cn(
                  'flex items-center gap-3 px-3 py-2 rounded-lg text-sm transition-colors',
                  isActive
                    ? 'bg-audius-purple/15 text-audius-purple font-medium'
                    : 'text-text-secondary hover:bg-tint-05'
                )}
              >
                <item.icon className="w-5 h-5" />
                {item.label}
              </Link>
            </li>
          );
        })}
      </ul>
    </nav>
  );
}

